package JJH;

public class Customer_info {

	private String Customer_ID;
	private String Customer_name;
	private String Customer_PWD;
	private String Customer_gender;
	private String Customer_mail;
	private String Customer_phone;
	private String Customer_state;

	public final String getCustomer_ID() {
		return Customer_ID;
	}

	public final void setCustomer_ID(String customer_ID) {
		Customer_ID = customer_ID;
	}

	public final String getCustomer_name() {
		return Customer_name;
	}

	public final void setCustomer_name(String customer_name) {
		Customer_name = customer_name;
	}

	public final String getCustomer_PWD() {
		return Customer_PWD;
	}

	public final void setCustomer_PWD(String customer_PWD) {
		Customer_PWD = customer_PWD;
	}

	public final String getCustomer_gender() {
		return Customer_gender;
	}

	public final void setCustomer_gender(String customer_gender) {
		Customer_gender = customer_gender;
	}

	public final String getCustomer_mail() {
		return Customer_mail;
	}

	public final void setCustomer_mail(String customer_mail) {
		Customer_mail = customer_mail;
	}

	public final String getCustomer_phone() {
		return Customer_phone;
	}

	public final void setCustomer_phone(String customer_phone) {
		Customer_phone = customer_phone;
	}

	public final String getCustomer_state() {
		return Customer_state;
	}

	public final void setCustomer_state(String customer_state) {
		Customer_state = customer_state;
	}

}