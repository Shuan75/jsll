package april23jjh;

public class Customer_info {
	private String cust_id;
	private String cust_name;
	private String cust_addr;
	private String cust_reg_date;

	public final String getCust_id() {
		return cust_id;
	}

	public final void setCust_id(String cust_id) {
		this.cust_id = cust_id;
	}

	public final String getCust_name() {
		return cust_name;
	}

	public final void setCust_name(String cust_name) {
		this.cust_name = cust_name;
	}

	public final String getCust_addr() {
		return cust_addr;
	}

	public final void setCust_addr(String cust_addr) {
		this.cust_addr = cust_addr;
	}

	public final String getCust_reg_date() {
		return cust_reg_date;
	}

	public final void setCust_reg_date(String cust_reg_date) {
		this.cust_reg_date = cust_reg_date;
	}

}
